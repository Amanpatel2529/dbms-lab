CREATE TABLE AREA (RADIUS NUMBER(2),AREA NUMBER(10,2));

DECLARE
AREA1 NUMBER(10,2);
RADIUS1 NUMBER(2):=5;
PI CONSTANT NUMBER(3,2):=3.14;

BEGIN
WHILE RADIUS1 <=9
LOOP
AREA1:=PI*RADIUS1*RADIUS1;
INSERT INTO AREA VALUES(RADIUS1,AREA1);
RADIUS1:=RADIUS1+1;

END LOOP;
END;

